    public String xacDinhCaLam(int gio) {
        if (gio >= 0 && gio < 6) {
            return "Ca Tối";
        } else if (gio >= 6 && gio < 12) {
            return "Ca Sáng";
        } else if (gio >= 12 && gio < 18) {
            return "Ca Trưa";
        } else {
            return "Ca Chiều";
        }
    }

    public void updateThongTin() {
        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm:ss");
        DateTimeFormatter formatter1 = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        DateTimeFormatter formatter2 = DateTimeFormatter.ofPattern("yyyy/dd/MM");
        String gioHienTai = now.format(formatter);
        String homNay = now.format(formatter1);
        String homNay1 = now.format(formatter2);
        formatter = DateTimeFormatter.ofPattern("EEEE", new Locale("vi")); // "EEEE" để lấy tên thứ
        thuHienTai = now.format(formatter);
        caLamHienTai = xacDinhCaLam(now.getHour());
        gioKT = gioHienTai;
        NgayHomNay = homNay;
        GetToDay = homNay1;
    }

