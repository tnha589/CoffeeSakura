    public void fillDataCalam() {
        if (caLamHienTai.equalsIgnoreCase("Ca Tối")) {
            gioBD = "00:00:00";
        } else if (caLamHienTai.equalsIgnoreCase("Ca Sáng")) {
            gioBD = "06:00:00";
        } else if (caLamHienTai.equalsIgnoreCase("Ca Trưa")) {
            gioBD = "12:00:00";
        } else {
            gioBD = "18:00:00";
        }
        List<Object[]> list = dao.getDataGiaoCa(gioBD, gioKT);
        if (list.size() != 0) {
            Object[] Col = list.get(0); // Lấy hàng đầu tiên từ danh sách
            lblTenCa.setText(caLamHienTai);
            lblTenThuNgan.setText(user);
            lblTienKet.setText(String.valueOf(TienTrongKet));
            double tongtienHD = (Double) Col[0];
            lblTongTienHD.setText(String.valueOf(tongtienHD));
            int SLHD = (Integer) Col[1];
            lblSoLuongHD.setText(String.valueOf(SLHD));
            double TongTienHDC = 0;
            if (Col[2] != null) {
                TongTienHDC = (Double) Col[2];
            }
            lblTongTienHDC.setText(String.valueOf(TongTienHDC));
            int SLHDC = (Integer) Col[3];
            lblSoLuongHDC.setText(String.valueOf(SLHDC));
            double TongTienMat = (Double) Col[4];
            lblTienMat.setText(String.valueOf(TongTienMat));
            double TongTienCK = 0;
            if (Col[5] != null) {
                TongTienCK = (Double) Col[5];
            }

            lblChuyenKhoan.setText(String.valueOf(TongTienCK));
            double doanhThu = 0;
            if (Col[6] != null) {
                doanhThu = (Double) Col[6];
            }

            lblDoanhThu.setText(String.valueOf(doanhThu));

        } else {
            lblTenCa.setText(caLamHienTai);
            lblTenThuNgan.setText(user);
            lblTienKet.setText(String.valueOf(TienTrongKet));
            lblTongTienHD.setText("0");
            lblSoLuongHD.setText("0");
            lblTongTienHDC.setText("0");
            lblSoLuongHDC.setText("0");
            lblTienMat.setText("0");
            lblChuyenKhoan.setText("0");
            lblDoanhThu.setText("0");
        }

    }

    public void fillTableNhanVien() {
        DefaultTableModel model1 = (DefaultTableModel) tblNhanVien.getModel();
        model1.setRowCount(0);
        int i = 0;
        boolean trangthai = false;
        List<Object[]> list = clDao.getNhanVienTrongCa(caLamHienTai, thuHienTai);
        for (Object[] row : list) {
            i++;
            if (String.valueOf(row[1]).equalsIgnoreCase("Vắng mặt")) {
                trangthai = false;
            } else {
                trangthai = true;
            }
            model1.addRow(new Object[]{i, row[0], trangthai, row[2]});
        }
    }

    public void fillTableLSCa() {
        DefaultTableModel model = (DefaultTableModel) tblLichSuCa.getModel();
        model.setRowCount(0);
        try {
            List<Object[]> list = dao.SelectALl();
            for (Object[] row : list) {
                model.addRow(new Object[]{row[0], row[1], row[2], row[3], row[4], row[5]});
                lsCa.add(new Object[]{row[0], row[1], row[2], row[3], row[4], row[5], row[6]});
            }
        } catch (Exception e) {
              getJOptionePane.methodThatUsesOptionPane(this, "Lỗi truy vấn dữ lịch sử giao ca!");
            e.printStackTrace();
        }
    }

